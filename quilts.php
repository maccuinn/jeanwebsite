                <h2>William's Quilt</h2>
                <p>Inspiration for this baby quilt came from the new parents. My son and his wife wanted race cars and I wanted an I-Spy quilt. The solution, two quilts folded into one. I created a fairly simple panel with zigzagging sashing in purple. After exhaustive searching I found a contrasting yellow Velcro. The sashing not only joined the I Spy panels but also became the race track. The dotted yellow line is formed from pieces of Velcro spaced down the middle. I created five different race cars, one for William, the new arrival, and one each for Mom, Dad, Grandpa and me. The base of the race cars were cut out of vinyl, the fabric was sewn on with raw edge appliqué and as a final step, I sewed the hook part of the Velcro to the race car backs. Then I created a pocket label to store the cars in when they weren’t on the track. To add more play value to the quilt, I used a pre-printed panel with city roads and of course another race track.</p>
