                <h2>Mt. Baker Night Owl</h2>
                <p>While driving to a cabin near Mt. Baker Washington, my friends and I overshot our destination. Before we knew it we were twenty miles past it up the narrow windy mountain road near the ski hill. Heading back down in the pitch dark, an owl swooped down into our headlights and was gone. When I got home, I searched the net for a picture to match my memory. The bird’s feathers were pieced with a fabric that featured grasses and moths. I even included some moths in the upper back of the bird. I thread painted the highlights of the moon on the owl’s wings. The background was strip pieced to give the illusion of forest.</p>
