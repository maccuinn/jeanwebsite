                <h2>Retro Roadtrip</h2>
                <p>When my husband and I attended the Vintage Car Club of Canada’s May tour at Qualicum Beach on Vancouver Island, one of the runs took us to Courtenay where we enjoyed a boxed lunch at the city park. At the entrance to the park, two totem poles stood guard. After carefully lining up our 1956 Chevy Belair nine passenger station wagon between the totems I snapped a photograph to add to my quilt ideas folder. When it came time to work on the quilt, I used a totem pole printed fabric. The size of the poles determined the final size of the quilt. Keeping true to the proportions in the photo I created a new background for the quilt. Then I enlarged the car in my photo to match, printed it on white cotton, added raw edged appliqué and thread painted it. I sewed the finished car onto my background. While the totem poles are from a fabric print, they are also extensively fabric painted.</p>
